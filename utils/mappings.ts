// Mappings for various Unicode pseudo-fonts
export interface CharMap {
  [key: string]: string;
}

const normal = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
const normalArr = normal.split('');

// Helper to generate map from array of codepoints or strings
const createMap = (source: string[], targets: string[]) => {
  const map: CharMap = {};
  source.forEach((char, index) => {
    if (targets[index]) {
      map[char] = targets[index];
    }
  });
  return map;
};

